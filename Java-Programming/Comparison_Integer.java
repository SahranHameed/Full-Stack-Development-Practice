// Comparison Operator Greathen and Lessthen 
public class Comparison_Integer {
    public static void main(String args[])

    {
        int number1 = 40;
        int number2 = 60;

        if (number1 > number2) {
            System.out.println("Number 1 is Greater");
        }
        else
        {
            System.out.print("Number 2 is Greater");
        }
    }
}


